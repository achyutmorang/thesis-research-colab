from dataclasses import dataclass
from pathlib import Path
from typing import Iterable, List, Optional, Sequence

import pandas as pd


@dataclass
class SmartFinetuneResult:
    status: str
    repo_dir: str
    run_dir: str
    train_config_path: str
    val_config_path: str
    ckpt_dir: str
    latest_ckpt_path: str
    train_command: str
    val_command: str
    train_raw_dirs: List[str]
    val_raw_dirs: List[str]
    message: str = ""
    train_rc: int = 0
    val_rc: int = 0


def _normalize_dirs(values: Optional[Sequence[str]], fallback: str) -> List[str]:
    if values is None:
        return [str(fallback)]
    out = [str(v).strip() for v in values if str(v).strip()]
    return out if len(out) > 0 else [str(fallback)]


def _run_command(cmd: List[str], cwd: Optional[str] = None) -> subprocess.CompletedProcess:
    print("[smart-ft] $", " ".join(cmd))
    return subprocess.run(cmd, cwd=cwd, check=False)


def _ensure_smart_repo(
    repo_dir: str,
    repo_url: str = "https://github.com/rainmaker22/SMART.git",
    branch: str = "main",
    update_if_exists: bool = False,
) -> Path:
    repo_path = Path(str(repo_dir)).expanduser()
    if not repo_path.exists():
        repo_path.parent.mkdir(parents=True, exist_ok=True)
        rc = _run_command(
            ["git", "clone", "--depth", "1", "-b", str(branch), str(repo_url), str(repo_path)],
            cwd=str(repo_path.parent),
        )
        if rc.returncode != 0:
            raise RuntimeError(f"Failed to clone SMART repo from {repo_url} to {repo_path}")
    elif update_if_exists:
        _run_command(["git", "pull", "--ff-only"], cwd=str(repo_path))
    return repo_path


def _yaml_list(items: Iterable[str]) -> str:
    return "[" + ", ".join([f'"{str(x)}"' for x in items]) + "]"


